package carManagerProject;

public class CarRepairListInfo {
    private String mechanicName;
    private String repairCarNumber;
    private String repairCarOwner;

    public String getMechanicName() {
        return mechanicName;
    }

    public void setMechanicName(String mechanicName) {
        this.mechanicName = mechanicName;
    }

    public String getRepairCarNumber() {
        return repairCarNumber;
    }

    public void setRepairCarNumber(String repairCarNumber) {
        this.repairCarNumber = repairCarNumber;
    }

    public String getRepairCarOwner() {
        return repairCarOwner;
    }

    public void setRepairCarOwner(String repairCarOwner) {
        this.repairCarOwner = repairCarOwner;
    }
}
